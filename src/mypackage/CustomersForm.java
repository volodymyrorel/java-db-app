    private void tableUpdate(String query) {
        int c;
        try {
            ps = conn.prepareStatement(query);
            ResultSet rs = ps.executeQuery();
            ResultSetMetaData meta = rs.getMetaData();
            c = meta.getColumnCount();
            DefaultTableModel df = (DefaultTableModel)jTable1.getModel();
            df.setRowCount(0);
            while(rs.next()) {
                Vector v = new Vector();
                for (int i = 1; i <= c; i++) {
                    v.add(rs.getString("passport_id"));
                    v.add(rs.getString("name_customer"));
                    v.add(rs.getString("email_customer"));
                    v.add(rs.getString("phone_customer"));
                }
                df.addRow(v);
            }
        } catch (SQLException ex) {
            Logger.getLogger(CustomersForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    
