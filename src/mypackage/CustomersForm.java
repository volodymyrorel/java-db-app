    private void buttonNewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonNewActionPerformed
        // TODO add your handling code here:
        String id = txtId.getText();
        String name = txtName.getText();
        String email = txtEmail.getText();
        String phone = txtPhone.getText();
        
        if (!"".equals(id) && !"".equals(name) && !"".equals(email) && !"".equals(phone)) {
        
            try {
                ps = conn.prepareStatement("INSERT INTO `customers`(passport_id, name_customer, email_customer, phone_customer) VALUES(?, ?, ?, ?);");
                ps.setString(1, id);
                ps.setString(2, name);
                ps.setString(3, email);
                ps.setString(4, phone);
                ps.executeUpdate();
                
                JOptionPane.showMessageDialog(this, "Додано замовника");
                tableUpdate("SELECT * FROM `customers`;");
                
                txtId.setText("");
                txtName.setText("");
                txtEmail.setText("");
                txtPhone.setText("");
                txtId.requestFocus();
                
            } catch (SQLException ex) {
                Logger.getLogger(CustomersForm.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_buttonNewActionPerformed

