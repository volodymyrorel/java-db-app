    private void buttonNewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonNewActionPerformed
        // TODO add your handling code here:
        String nameText = txtNameAgent.getText();
        String emailText = txtEmailAgent.getText();
        String phoneText = txtPhoneAgent.getText();
        
        if (!"".equals(nameText) && !"".equals(emailText) && !"".equals(phoneText)) {
        
            try {
                ps = conn.prepareStatement("INSERT INTO `agents`(name_agent, email_agent, phone_agent) VALUES(?, ?, ?);");
                ps.setString(1, nameText);
                ps.setString(2, emailText);
                ps.setString(3, phoneText);
                ps.executeUpdate();
                
                JOptionPane.showMessageDialog(this, "Додано агента");
                tableUpdate("SELECT * FROM `agents`;");
                
                txtNameAgent.setText("");
                txtEmailAgent.setText("");
                txtPhoneAgent.setText("");
                txtNameAgent.requestFocus();
                
            } catch (SQLException ex) {
                Logger.getLogger(AgentsForm.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_buttonNewActionPerformed

