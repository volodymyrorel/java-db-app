    private void tableUpdate(String query) {
        int c;
        try {
            ps = conn.prepareStatement(query);
            ResultSet rs = ps.executeQuery();
            ResultSetMetaData meta = rs.getMetaData();
            c = meta.getColumnCount();
            DefaultTableModel df = (DefaultTableModel)jTable1.getModel();
            df.setRowCount(0);
            while(rs.next()) {
                Vector v = new Vector();
                for (int i = 1; i <= c; i++) {
                    v.add(rs.getString("name_agent"));
                    v.add(rs.getString("email_agent"));
                    v.add(rs.getString("phone_agent"));
                }
                df.addRow(v);
            }
        } catch (SQLException ex) {
            Logger.getLogger(AgentsForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    
